#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const dist = path.join(root, "dist");

function walk(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const full = path.join(dir, entry.name);
    return entry.isDirectory() ? walk(full) : full.endsWith(".js") ? [full] : [];
  });
}

function hasKnownExtension(specifier) {
  return /\.(?:js|mjs|cjs|json|node)$/i.test(specifier);
}

function toPosix(specifier) {
  return specifier.replace(/\\/g, "/");
}

function resolveSpecifier(file, specifier) {
  if (!specifier.startsWith(".") || hasKnownExtension(specifier)) return specifier;

  const absolute = path.resolve(path.dirname(file), specifier);
  if (fs.existsSync(`${absolute}.js`)) return toPosix(`${specifier}.js`);
  if (fs.existsSync(path.join(absolute, "index.js"))) return toPosix(`${specifier}/index.js`);
  return specifier;
}

for (const file of walk(dist)) {
  const original = fs.readFileSync(file, "utf8");
  const rewritten = original
    .replace(/(\bfrom\s*["'])(\.[^"']+)(["'])/g, (_match, prefix, specifier, suffix) => {
      return `${prefix}${resolveSpecifier(file, specifier)}${suffix}`;
    })
    .replace(/(\bimport\s*\(\s*["'])(\.[^"']+)(["']\s*\))/g, (_match, prefix, specifier, suffix) => {
      return `${prefix}${resolveSpecifier(file, specifier)}${suffix}`;
    });

  if (rewritten !== original) fs.writeFileSync(file, rewritten);
}

// ---------------------------------------------------------------------------
// Generate compiled cold-start probe from TypeScript source
// ---------------------------------------------------------------------------
const coldStartSrc = path.join(root, "scripts", "performance-baseline-cold-start.ts");
const coldStartOut = path.join(root, "scripts", "performance-baseline-cold-start-compiled.mjs");

if (fs.existsSync(coldStartSrc)) {
  const tsSource = fs.readFileSync(coldStartSrc, "utf8");
  const mjsContent = tsSource
    // Strip shebang (handle CRLF)
    .replace(/^#!.*\r?\n/, "")
    // Rewrite src/ imports to dist/ and add .js extension (static and dynamic)
    .replace(/((?:from|import\()\s*["'])\.\.\/src\/([^"'\r\n]+)(["'])/g, '$1../dist/$2.js$3')
    // Remove `as const` assertions
    .replace(/\s+as\s+const/g, "")
    // Remove TypeScript type annotations from parameters and return types
    .replace(/:\s*Promise<void>/g, "")
    .replace(/:\s*unknown/g, "")
    // Remove the async wrapper — use top-level await like the original compiled version
    .replace(
      /async function main\(\)\s*\{([\s\S]*?)\}\s*\r?\nmain\(\)\.catch[\s\S]*$/,
      (_, body) => body.replace(/^ {2}/gm, "").trim()
    )
    .trim() + "\n";

  fs.writeFileSync(coldStartOut, `/**\n * Auto-generated by fix-dist-esm-imports.js during build.\n * Do not edit — modify performance-baseline-cold-start.ts instead.\n */\n\n${mjsContent}`);
}

