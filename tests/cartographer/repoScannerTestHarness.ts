function normalizedFromRoot(repoRoot: string, absolutePath: string): string {
  const relativePath = path.relative(repoRoot, absolutePath);
  return relativePath === "" ? "." : relativePath.split(path.sep).join("/").replace(/\\/g, "/");
}
