export function makeSpyingReader(repoRoot: string, failures: readonly ReaderFailure[] = []): { readonly reader: FileReader; readonly headCalls: readonly FileReadCall[]; readonly allCalls: readonly FileReadCall[] } {
  const headCalls: FileReadCall[] = [];
  const allCalls: FileReadCall[] = [];
  const failureFor = (method: ReaderMethod, absolutePath: string): ReaderFailure | undefined =>
    failures.find((failure) => failure.method === method && failure.normalizedPath === normalizedFromRoot(repoRoot, absolutePath));
  const reader: FileReader = {
    async lstat(absolutePath: string): Promise<Stats> {
      const failure = failureFor("lstat", absolutePath);
      if (failure !== undefined) {
        throw new Error(failure.message);
      }
      return defaultFileReader.lstat(absolutePath);
    },
    async readdir(absolutePath: string): Promise<readonly Dirent[]> {
      const failure = failureFor("readdir", absolutePath);
      if (failure !== undefined) {
        throw new Error(failure.message);
      }
      return defaultFileReader.readdir(absolutePath);
    },
    async readHead(absolutePath: string, maxBytes: number): Promise<Uint8Array> {
      const failure = failureFor("readHead", absolutePath);
      if (failure !== undefined) {
        throw new Error(failure.message);
      }
      headCalls.push({ normalizedPath: normalizedFromRoot(repoRoot, absolutePath), maxBytes });
      return defaultFileReader.readHead(absolutePath, maxBytes);
    },
    async readAll(absolutePath: string): Promise<Uint8Array> {
      const failure = failureFor("readAll", absolutePath);
      if (failure !== undefined) {
        throw new Error(failure.message);
      }
      allCalls.push({ normalizedPath: normalizedFromRoot(repoRoot, absolutePath) });
      return defaultFileReader.readAll(absolutePath);
    },
  };
  return { reader, headCalls, allCalls };
}

export function labelForEvent(event: CartographerScanEvent): ScanEventLabel {
  switch (event.type) {
    case "CARTOGRAPHER_SCAN_STARTED":
      return { type: event.type, path: event.repoRoot };
    case "CARTOGRAPHER_FILE_INDEXED":
    case "CARTOGRAPHER_FILE_DELETED":
      return { type: event.type, path: event.path };
    case "CARTOGRAPHER_FILE_IGNORED":
      return { type: event.type, path: event.ignoredFile.path };
    case "CARTOGRAPHER_SCAN_COMPLETED":
      return { type: event.type, path: event.summary.repoRoot };
    case "CARTOGRAPHER_SCAN_FAILED":
      return { type: event.type, path: event.error.path };
    default: {
      const unreachable: never = event;
      return unreachable;
    }
  }
}

export function collectEvents(): { readonly events: readonly CartographerScanEvent[]; readonly emitter: CartographerScanEmitter } {
  const events: CartographerScanEvent[] = [];
  return {
    events,
    emitter: (event) => {
      events.push(event);
    },
  };
}

function stripFileVolatile(file: FileNode): StableFileNode {
  const { lastIndexedAt: _lastIndexedAt, ...stableFile } = file;
  return stableFile;
}

function compareUtf16(left: string, right: string): number {
  return left < right ? -1 : left > right ? 1 : 0;
}

async function writeFixtureFile(repoRoot: string, normalizedPath: string, contents: string): Promise<void> {
  const absolutePath = path.join(repoRoot, ...normalizedPath.split("/"));
  await fs.mkdir(path.dirname(absolutePath), { recursive: true });
  await fs.writeFile(absolutePath, contents, "utf8");
}

export async function writeOversizedFile(repoRoot: string, normalizedPath: string): Promise<void> {
  const absolutePath = path.join(repoRoot, ...normalizedPath.split("/"));
  await fs.mkdir(path.dirname(absolutePath), { recursive: true });
  const size = DEFAULT_MAX_FILE_SIZE_BYTES + 1024;
  await fs.writeFile(absolutePath, Buffer.alloc(size, 0x41));
}

