type ReaderMethod = "lstat" | "readdir" | "readHead" | "readAll";
type ReaderFailure = { readonly method: ReaderMethod; readonly normalizedPath: string; readonly message: string };
type StableFileNode = Omit<FileNode, "lastIndexedAt">;
type StableRepoSnapshot = Omit<RepoSnapshot, "createdAt">;
type StableScanResult = Omit<ScanResult, "files" | "changedFiles" | "snapshot"> & {
  readonly snapshot: StableRepoSnapshot;
  readonly files: readonly StableFileNode[];
  readonly changedFiles: readonly StableFileNode[];
};

export function stripVolatile(result: ScanResult): StableScanResult {
  const { createdAt: _createdAt, ...snapshot } = result.snapshot;
  return {
    ...result,
    snapshot,
    files: result.files.map(stripFileVolatile),
    changedFiles: result.changedFiles.map(stripFileVolatile),
  };
}

export function isSortedUtf16(values: readonly string[]): boolean {
  return values.every((value, index) => index === 0 || compareUtf16(values[index - 1] ?? "", value) <= 0);
}

